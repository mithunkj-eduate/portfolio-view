import axios from "axios";
import React, { useContext, useEffect, useState } from "react";
import { useParams } from "react-router-dom";
import { AppContext } from "../context/context";
import { payloadTypes } from "../context/reducer";

function useProffionPortfolio() {
  const apiBaseUrl = import.meta.env.VITE_PRODUCTION_BASE_URL;
  const { dispatch } = useContext(AppContext);

  const [data, setData] = useState(null);
  const { id } = useParams();

  useEffect(() => {
    // Example usage (e.g., in an API file or API call)

    const getUser = async () => {
      try {
        const res = await axios.get(
          `${apiBaseUrl}/api/professionalPortfolio/${id}`,
        );
        if (res && res.data) {
          setData(res.data);
          dispatch({
            type: payloadTypes.SET_PORTFOLIO_TYPE,
            payload: {
              portfolioType: res.type,
            },
          });
        }
      } catch (error) {
        console.log(error);
      }
    };
    if (id !== "kiran-farmer") {
      getUser();
    }
  }, [apiBaseUrl, id]);

  const result = id === "kiran-farmer" ? farmerData : data;

  if (result) return result;
}

export default useProffionPortfolio;
